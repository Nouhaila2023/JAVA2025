package Ejercicios_Udemy.Variables;

public class PrimitivosFloat {
    public static void main(String[] args) {

        float realFloat1 = 1.7f ;
        System.out.println("realFloat1: " + realFloat1);

        float realFloat2 = 2.1223e3f;   //2122.3 mover la como hace la esquierda
        System.out.println("realFloat2 = " + realFloat2);

        float realFloat3 = 2122.3e-3f;   //2.1223 mover la coma hace la derecha
        System.out.println("realFloat3 = " + realFloat3);






    }
}
